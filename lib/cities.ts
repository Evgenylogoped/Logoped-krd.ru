// Канонизатор названий городов по стилю ГОСТ/Росреестр
function normalizeCityCanon(input: string): string {
  let s = String(input || '')
  // убрать служебные пометки в конце: «Оспаривается» (возможны пробелы)
  s = s.replace(/\s*Оспаривается\s*$/u, '')
  // заменить множественные пробелы и нормализовать дефисы без пробелов вокруг
  s = s.replace(/\s+/g, ' ')
  s = s.replace(/\s*-\s*/g, '-')
  s = s.replace(/-+/g, '-')
  s = s.trim()
  if (!s) return ''
  // токены со списком «малых слов», которые пишутся строчными (между пробелами или дефисами)
  const small = new Set(['на','и','по','во','при','в','с','со','об','от','до','за','из','у','о','над','под','для','без'])
  // Функция тайтл-кейса с учётом русской локали и «ё/е» (сохраняем как в источнике, меняем только регистр)
  function titleCase(word: string): string {
    const w = word.toLocaleLowerCase('ru')
    if (!w) return ''
    return w[0].toLocaleUpperCase('ru') + w.slice(1)
  }
  // Применяем к словам и частям через дефис
  const words = s.split(' ')
  const normWords = words.map(w => {
    const parts = w.split('-')
    const normParts = parts.map(p => {
      const lower = p.toLocaleLowerCase('ru')
      if (small.has(lower)) return lower
      return titleCase(p)
    })
    return normParts.join('-')
  })
  return normWords.join(' ')
}

// Очистка и де-дупликация
export const RU_CITIES: string[] = Array.from(new Set(
  RAW_CITIES.map(normalizeCityCanon)
)).filter(Boolean)

export function isValidCity(city?: string | null): boolean {
  if (!city) return false
  const c = city.trim()
  if (!c) return false
  return RU_CITIES.some(x => x.toLowerCase() === c.toLowerCase())
}

export function normalizeCity(city: string): string {
  const found = RU_CITIES.find(x => x.toLowerCase() === city.trim().toLowerCase())
  return found || city.trim()
}
